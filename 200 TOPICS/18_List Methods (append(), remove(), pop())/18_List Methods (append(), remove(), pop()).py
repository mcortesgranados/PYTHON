# Creating a list
my_list = [1, 2, 3, 4, 5]
print("Original list:", my_list)

# append() method
my_list.append(6)
print("List after appending 6:", my_list)

# remove() method
my_list.remove(3)
print("List after removing 3:", my_list)

# pop() method
popped_element = my_list.pop()
print("Popped element:", popped_element)
print("List after popping:", my_list)
