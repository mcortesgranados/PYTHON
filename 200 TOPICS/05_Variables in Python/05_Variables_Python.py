# Variable declaration and assignment
my_variable = 42
another_variable = "Hello, world!"

# Printing the variables
print("my_variable:", my_variable)
print("another_variable:", another_variable)

# Variable reassignment
my_variable = 100
another_variable = "Goodbye!"

# Printing the updated variables
print("my_variable (updated):", my_variable)
print("another_variable (updated):", another_variable)
