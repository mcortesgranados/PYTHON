# Numeric variables
x = 10
y = 3

# Addition
addition_result = x + y
print("Addition:", addition_result)

# Subtraction
subtraction_result = x - y
print("Subtraction:", subtraction_result)

# Multiplication
multiplication_result = x * y
print("Multiplication:", multiplication_result)

# Division (classic division)
division_result = x / y
print("Division (classic):", division_result)

# Division (floor division)
floor_division_result = x // y
print("Division (floor):", floor_division_result)

# Modulus (remainder)
modulus_result = x % y
print("Modulus:", modulus_result)

# Exponentiation
exponentiation_result = x ** y
print("Exponentiation:", exponentiation_result)

# Increment
x += 1
print("Increment:", x)

# Decrement
y -= 1
print("Decrement:", y)
