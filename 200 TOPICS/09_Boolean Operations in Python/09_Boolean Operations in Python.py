# Boolean variables
x = True
y = False

# Logical AND
result_and = x and y
print("Logical AND:", result_and)

# Logical OR
result_or = x or y
print("Logical OR:", result_or)

# Logical NOT
result_not_x = not x
result_not_y = not y
print("Logical NOT (x):", result_not_x)
print("Logical NOT (y):", result_not_y)

# Comparison operators
a = 10
b = 20

# Equality
result_equal = a == b
print("Equality:", result_equal)

# Inequality
result_not_equal = a != b
print("Inequality:", result_not_equal)

# Greater than
result_greater_than = a > b
print("Greater than:", result_greater_than)

# Less than
result_less_than = a < b
print("Less than:", result_less_than)

# Greater than or equal to
result_greater_equal = a >= b
print("Greater than or equal to:", result_greater_equal)

# Less than or equal to
result_less_equal = a <= b
print("Less than or equal to:", result_less_equal)
