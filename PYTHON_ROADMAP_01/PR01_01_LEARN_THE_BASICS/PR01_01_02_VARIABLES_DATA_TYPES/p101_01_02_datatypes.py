# This program demonstrates using different data types in Python

# Integer (whole number)
age = 30

# Float (decimal number)
pi = 3.14159

# String (text)
name = "Alice"

# Boolean (True or False)
is_sunny = True

# List (ordered collection of items)
fruits = ["apple", "banana", "orange"]

# Check the data types of variables
print("Age (int):", type(age))
print("Pi (float):", type(pi))
print("Name (string):", type(name))
print("Is sunny (bool):", type(is_sunny))
print("Fruits (list):", type(fruits))

# Accessing elements in a list
first_fruit = fruits[0]  # Accessing by index (starts from 0)

print("The first fruit is:", first_fruit)
