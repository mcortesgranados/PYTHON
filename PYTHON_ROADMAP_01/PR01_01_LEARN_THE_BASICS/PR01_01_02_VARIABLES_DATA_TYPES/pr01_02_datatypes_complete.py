# Python Data Types Example

# Example 1: Integers
# Integers represent whole numbers without any decimal point.
# They can be positive, negative, or zero.
integer_var = 42

# Example 2: Floating-Point Numbers
# Floating-point numbers represent real numbers with a decimal point.
# They can be specified using a decimal point or scientific notation.
float_var = 3.14

# Example 3: Strings
# Strings represent sequences of characters enclosed within single, double, or triple quotes.
# They can contain letters, digits, symbols, and spaces.
string_var = "Hello, World!"

# Example 4: Lists
# Lists are ordered collections of items that can be of different data types.
# They are mutable, meaning their elements can be modified.
list_var = [1, 2, 3, 'apple', 'banana', 'cherry']

# Example 5: Tuples
# Tuples are ordered collections similar to lists, but they are immutable.
# Once created, the elements of a tuple cannot be modified.
tuple_var = (1, 2, 3, 'apple', 'banana', 'cherry')

# Example 6: Dictionaries
# Dictionaries are unordered collections of key-value pairs.
# Each key is associated with a value, and keys must be unique within a dictionary.
# They are mutable and can store heterogeneous data types.
dict_var = {'name': 'John', 'age': 30, 'city': 'New York'}

# Example 7: Sets
# Sets are unordered collections of unique elements.
# They are mutable, but individual elements cannot be modified.
# Sets are useful for mathematical operations like union, intersection, etc.
set_var = {1, 2, 3, 4, 5}

# Example 8: Booleans
# Booleans represent truth values: True or False.
# They are often used in conditional and logical expressions.
bool_var = True

# Example 9: None
# None is a special constant in Python that represents the absence of a value.
# It is often used as a placeholder or to indicate that a variable has no value.
none_var = None

